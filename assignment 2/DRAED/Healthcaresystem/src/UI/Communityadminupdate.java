    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        txtname.setText(this.emp.getName());
        txtage.setText(String.valueOf(this.emp.getAge()));
        txtgender.setText(this.emp.getGender());
        txtresidence.setText(this.emp.getResidence());
        txtcity.setText(this.emp.getCity());
        txtcommunity.setText(this.emp.getCommunity());
        
        txtname.setEditable(false);
        txtage.setEditable(false);
        txtgender.setEditable(false);
        txtresidence.setEditable(false);
        txtcity.setEditable(false);
        txtcommunity.setEditable(false);
       
    }//GEN-LAST:event_formWindowOpened

