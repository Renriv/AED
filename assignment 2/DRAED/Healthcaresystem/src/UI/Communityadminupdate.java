    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        
        emp.setName(txtname.getText());
        emp.setAge(Integer.parseInt(txtage.getText()));
        emp.setGender(txtgender.getText());
        emp.setResidence(txtresidence.getText());
        emp.setCity(txtcity.getText());
        emp.setCommunity(txtcommunity.getText());

        JOptionPane.showMessageDialog(this,"Value Updated");
    }//GEN-LAST:event_btnsaveActionPerformed

