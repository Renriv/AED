public void search(String str)
    {
        DefaultTableModel model = (DefaultTableModel) tblcomview.getModel();
        TableRowSorter<DefaultTableModel> s = new TableRowSorter<>(model);
        tblcomview.setRowSorter(s);
        s.setRowFilter(RowFilter.regexFilter(str));
        
    }
