    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int selectedrow = tblcomview.getSelectedRow();
        if(selectedrow < 0)
        {
            JOptionPane.showMessageDialog(this, "Select a row to Delete");
        }
        else
        {
            DefaultTableModel model =(DefaultTableModel) tblcomview.getModel();
            Communitydetails emp = (Communitydetails)model.getValueAt(selectedrow,6);
            dict.deleteCommunitydetails(emp);
            JOptionPane.showMessageDialog(this, "Record deleted Successfully");
            Table();
        }
    }//GEN-LAST:event_btndeleteActionPerformed

