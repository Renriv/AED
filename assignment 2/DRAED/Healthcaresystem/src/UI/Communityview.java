   private void Table()
   {
       DefaultTableModel model = (DefaultTableModel) tblcomview.getModel();
       model.setRowCount(0);
       for (Communitydetails emp: dict.getCommunitydetailsinfo())
       {
           Object[] row=new Object[7];
           row[0]=emp.getName();
           row[1]=emp.getAge();
           row[2]=emp.getGender();
           row[3]=emp.getResidence();
           row[4]=emp.getCity();
           row[5]=emp.getCommunity();
           row[6]=emp;
           model.addRow(row);
       }
   }
